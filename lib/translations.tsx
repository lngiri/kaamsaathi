// Helper function to get bilingual text
export const t = (key: string, lang: 'en' | 'np' = 'en') => {
  const keys = key.split('.')
  let obj: any = translations

  for (const k of keys) {
    obj = obj?.[k]
    if (!obj) return key // Fallback to key if not found
  }

  return obj?.[lang] || obj?.en || key
}

// Helper to render both languages
export const BiText = ({
  en,
  np,
  enSize = '15px',
  npSize = '13px',
  className = '',
}: {
  en: string
  np: string
  enSize?: string
  npSize?: string
  className?: string
}) => {
  return (
    <div className={className}>
      <div style={{ fontSize: enSize }}>{en}</div>
      <div style={{ fontSize: npSize, color: '#666' }}>{np}</div>
    </div>
  )
}
